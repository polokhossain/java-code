

package basic.java;


public class Assignooperatoer {
    public static void main(String[] args) {
        int x=5;
        int y=2;
                
                
        x += y;
        System.out.println("x="+ x);// x=x+y
                
        x -= y;
        System.out.println("x="+ x);//x=x-y
                
        x *= y;
        System.out.println("x="+ x);//x=x*y
                
        x /= y;
        System.out.println("x="+ x);//x=x/y
                
        x %= y;
        System.out.println("x="+ x);//x=x%y
                
        
    }
    
}
